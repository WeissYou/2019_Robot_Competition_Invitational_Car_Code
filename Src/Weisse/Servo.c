/*************************************
  * 函数功能: 舵机初始化
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明: 无
  ************************************/
void Servo_Init(void)
{
  /* 启动通道PWM输出 */
  HAL_TIM_PWM_Start(&htim9, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim9, TIM_CHANNEL_2);
}

/*************************************
  * 函数功能: 机械臂控制
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明: 无
  ************************************/
void Arm_Control(uint8_t action)
{
  __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_1, action);
}

/*************************************
  * 函数功能: 卡爪控制
  * 输入参数: 无
  * 返 回 值: 无
  * 说    明: 无
  ************************************/
void Paw_Control(uint8_t action)
{
  __HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_2, action);
}

